public class While10000 {
//-----------------------------------------------------------------------------
    private static final int MAXIMUM = 10000;
//-----------------------------------------------------------------------------
    public static void main(String[] args) {

        int sum = 0;
        int number = 0;

        while (sum <= MAXIMUM) {
            sum += number;
            System.out.println("Number is " + number + ", sum is " + sum);
            number++;
        }

    }
//-----------------------------------------------------------------------------
}
