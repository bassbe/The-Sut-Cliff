//=============================================================================
public class Salary {
//-----------------------------------------------------------------------------
    private double salary;
//-----------------------------------------------------------------------------
    public Salary() {

        salary = 0.0;
    }
//-----------------------------------------------------------------------------
    public Salary(double theSalary) {

        salary = theSalary;
    }
//-----------------------------------------------------------------------------
    public String toString() {

        return(String.format("$%.2f",salary));
    }
//-----------------------------------------------------------------------------
}
//=============================================================================