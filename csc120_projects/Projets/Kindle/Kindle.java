public class Kindle {
//-----------------------------------------------------------------------------
    int pages, pageNumReached;
//-----------------------------------------------------------------------------
    public Kindle(int pages){
      this.pages = pages;
      pageNumReached = 1;
    
    }

//-----------------------------------------------------------------------------
    public void display(String prefix) {
     
      System.out.println(prefix + " you are on page " + pageNumReached+ " of " + pages);
     
       
    }
//-----------------------------------------------------------------------------
    public void turnPage( ) {

        pageNumReached++;   
    
    }
//-----------------------------------------------------------------------------
}
